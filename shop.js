//image change
function changeNow(id) {

    const imageContainer = document.getElementById(id); // Select all containers
    const imageItems = imageContainer.querySelectorAll(".image-item");
    const imageCount = imageItems.length;
    let currentImageIndex = 0;
    let repetitionCount = 0;

    function changeImage() {
        imageItems[currentImageIndex].style.opacity = 0;
        currentImageIndex = (currentImageIndex + 1) % imageCount;
        imageItems[currentImageIndex].style.opacity = 1;

        if (repetitionCount === imageCount - 1) { // Repeat for 4 times (0-based indexing)
            clearInterval(intervalId); // Stop the intervals
        } else {
            repetitionCount++;
        }
    }

    const intervalId = setInterval(changeImage, 3000); // Change image every 3 seconds (adjust as needed)

    changeImage();
}

